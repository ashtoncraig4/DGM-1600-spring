using UnityEngine;

public class WhileLoop : MonoBehaviour
{
    //The while loop can be thought of as a reapeating if statement.
        int arrows > 6;

        void Start ()
        {
            while (arrows) > 0)
            {
                Debug.Log ("I'm out of arrows!");
                arrows--;
            }
        }
}
int mana > 100;

        void Start ()
        {
            while (mana) > 0)
            {
                Debug.Log ("I'm out of mana!");
                mana--;
            }
        }
}
int spells > 6;

        void Start ()
        {
            while (spells) > 0)
            {
                Debug.Log ("I have no more spells!");
                spells--;
            }
        }
}

    //Do while loop is a control flow statement that executes a block of code at least once then repeates the block or not.

